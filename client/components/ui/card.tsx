import * as React from "react";

import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "outline" | "elevated" | "flat";
  }
>(({ className, variant = "default", ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "bg-card text-card-foreground transition-all duration-200 rounded-sm",
      {
        // SHARP BORDERS - No shadows (medical precision)
        default: "border border-border",
        outline: "border-2 border-[hsl(0,80%,50%)]",
        elevated: "border border-border shadow-md hover:shadow-lg",
        flat: "bg-transparent",
      }[variant],
      className,
    )}
    {...props}
  />
));
Card.displayName = "Card";

